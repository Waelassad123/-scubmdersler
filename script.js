// JavaScript kodunun geri kalanı aynı kalacak
const semesterButtons = document.querySelectorAll('.semester-button');
const courseList = document.getElementById('course-list');
const selectedSemester = document.getElementById('selected-semester');
const coursesContainer = document.getElementById('courses');
const courseDetails = document.getElementById('course-details');
const detailsContainer = document.getElementById('details');
const backButton = document.getElementById('back-button');
//Devtools'a erişim engelleme
document.addEventListener("contextmenu", (e) => e.preventDefault());
document.addEventListener("keydown", (e) => {
    if (e.key === "F12" || (e.ctrlKey && e.shiftKey && e.key === "I")) {
        e.preventDefault();
    }
});


// Sınıf ve yarıyıl butonlarına tıklama olayı ekle
semesterButtons.forEach(button => {
    button.addEventListener('click', () => {
        const semester = button.getAttribute('data-semester');
        selectedSemester.textContent = `${semester.replace('-', ' ').replace('guz', 'Sınıf Güz').replace('bahar', 'Sınıf Bahar')}`;
        coursesContainer.innerHTML = '';
        
        // Seçilen döneme ait dersleri listele
        coursesData[semester].forEach(course => {
            const courseCard = document.createElement('div');
            courseCard.className = 'card';
            courseCard.innerHTML = `<h3>${course.name}</h3><button class="details-button">Detaylar</button>`;
            coursesContainer.appendChild(courseCard);

            // Detaylar butonuna tıklama olayı
            courseCard.querySelector('.details-button').addEventListener('click', () => {
                detailsContainer.innerHTML = `
                    <h4>${course.name}</h4>
                    <p>${course.details}</p>
                    <p><strong>Dersin Hocası:</strong> ${course.instructor}</p>
                    <p><strong>Dersin İçeriği:</strong> ${course.generalInfo}</p>
                    ${course.pdfUrl ? `<p><strong>Dersin Notları:</strong> <a href="${course.pdfUrl}" target="_blank">Buraya Tıklayın</a></p>` : ''}
                `;
                courseList.classList.add('hidden');
                courseDetails.classList.remove('hidden');
            });
            
        });

        // Ders listesinin görünmesini sağla
        courseList.classList.remove('hidden');

        // Cihaz mobil ise listeyi butonun altına kaydır
        if (window.innerWidth <= 768) { // Mobil ekran boyutları için kontrol
            coursesContainer.scrollIntoView({ behavior: 'smooth', block: 'start' });
        }
    });
});

// Geri butonuna tıklama olayı
backButton.addEventListener('click', () => {
    courseDetails.classList.add('hidden');
    courseList.classList.remove('hidden');
}
);
